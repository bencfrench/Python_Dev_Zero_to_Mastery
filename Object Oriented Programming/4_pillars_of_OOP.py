# 4 Pillars of OOP
# 	1) Encapsulation
# 		Binding of data and functions to manipulate that data
#  		Encapsulate into one big object
# 	2) Abstraction
#  		Abstracting away info and giving access to only necessities
#  	3) Inheritance
# 		# new object take on the properties of existing objects
# 		# i.e. inherit objects
# 		# isinstance: you can check instances of classes with subclasses
# 	4) Polymorphism
# 		functions can take on different forms because of the object passed into it